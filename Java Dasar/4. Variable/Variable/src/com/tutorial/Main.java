package com.tutorial;

public class Main {

    public static void main(String[] args) {

        // Variable adalah huruf yang ada di matematika, misalnya x,y, atau z

        int x = 10; // assignment
        System.out.println("Nilai x adalah " + x);

        x = 29; // mengubah nilai x baru dalam assignment
        System.out.println("Nilai x yang baru adalah " + x);

        // Deklarasi
        int y;
        y = 21 + x;
        System.out.println("Nilai y adalah " + y);

    }

}
